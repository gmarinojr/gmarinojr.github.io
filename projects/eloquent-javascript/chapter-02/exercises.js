function triangles(hashtag) {

  for (var i = 1; i <= hashtag; i++) {
    var hash = '';
    for (var j = 0; j < i; j++) {
      hash += '#';
    }
    console.log(hash);
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (let n = 1; n <= 15; n++) {
    let output = "";
    if (n % 3 == 0) output += "fizz";
    if (n % 5 == 0) output += "buzz";
    console.log(output || n);
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  let board = "";
  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      if ((x + y) % 2 == 0) {
        board += " ";
      }
      else {
        board += "#";
      }
    }
    board += "\n";
  }
  console.log(board);
}

