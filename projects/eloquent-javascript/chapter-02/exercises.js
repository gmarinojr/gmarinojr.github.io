function drawChessboard(size) {
  // make a result string
  let result = '';
  // ' # # # #/n# # # # /n'
  for (let i = 0; i < size; i++) {
    // concat characters to the string
    if (i % 2 === 0) {
      for (let j = 0; j < size; j++) {
        if (j % 2 === 0) {
          result += ' ';
        } else {
          result += '#';
        }
      }
    } else {
      for (let j = 0; j < size; j++) {
        if (j % 2 === 0) {
          result += '#';
        } else {
          result += ' ';
        }
      }
    }
    result += '\n';
  }
  // return result
  return result;
}

